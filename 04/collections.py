# creates list of three fruits and assigns it to var myFruitList
myFruitList = ["apple", "banana", "cherry"]
# prints the list of fruits
print(myFruitList)
# prints the data type of myFruitList
print(type(myFruitList))

# prints the first fruit in the list
print(myFruitList[0])
# prints the second fruit in the list
print(myFruitList[1])
# prints the third fruit in the list
print(myFruitList[2])

# changes the third fruit in the list to "orange"
myFruitList[2] = "orange"
# prints updated list of fruits
print(myFruitList)

