# print str to console
print("Python has three numeric types: int, float, and complex")
# assigns int value 1 to myvalue
myvalue = 1
# prints value of myvalue
print(myvalue)
# prints data type of myvalue
print(type(myvalue))
# prints value of myvalue and data type of myvalue
print(str(myvalue) + " is of the data type " + str(type(myvalue)))

# assigns float value 3.4 to myvalue
myValue = 3.4
# prints value of myvalue
print(myValue)
# prints data type of myvalue
print(type(myValue))
# prints value of myvalue and data type of myvalue
print(str(myValue) + " is of the data type " + str(type(myValue)))

